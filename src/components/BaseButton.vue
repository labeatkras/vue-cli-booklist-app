<template>
  <button :class="styling" @click="$emit('btn-clicked')"><slot /></button>
</template>

<script>
export default {
  emits: ["btn-clicked"],
  props: {
    variant: {
      type: String,
      default: "primary",
      validator(propValue) {
        return ["primary", "secondary"].includes(propValue);
      },
    },
    isBookmarked: {
      type: Boolean,
      default: false,
    },
  },
  computed: {
    styling() {
      const styles = ["btn", `btn--${this.variant}`];
      if (this.isBookmarked) {
        styles.push("btn--bookmarked");
      }
      return styles;
    },
  },
};
</script>

